#include "libft.h"

int		ft_isspace(int c)
{
	if ((char)c == '\t' || (char)c == '\n' || (char)c == '\v' || (char)c == '\f'
			|| (char)c == '\r' || (char)c == ' ')
		return (1);
	else
		return (0);
}
